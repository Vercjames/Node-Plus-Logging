module.exports = {

  // Logging || Hex Generating Script
  // =-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
  // =-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
  generateId(num) {
    if (typeof num === "boolean") {
      if (!num) {
        return 'Null RequestId'
      }
      return crypto.randomBytes(10).toString('hex');
    } else {
      return crypto.randomBytes(num/2).toString('hex');
    }
  }
};
